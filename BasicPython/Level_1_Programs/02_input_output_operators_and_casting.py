"""Level 1: Input/output, operators, and type casting."""

print("Welcome to Python!")

num1 = 10
num2 = 3
print("Arithmetic:", num1 + num2, num1 - num2, num1 * num2, num1 / num2)
print("Floor division:", num1 // num2)
print("Modulus:", num1 % num2)
print("Exponent:", num1 ** 2)

print("Comparisons:", num1 > num2, num1 == num2)
print("Logical:", (num1 > 5) and (num2 < 5))

value = "42"
print("String to int:", int(value) + 8)
print("Int to float:", float(num1))
print("Float to string:", str(3.14))
print("String to bool:", bool("python"))
